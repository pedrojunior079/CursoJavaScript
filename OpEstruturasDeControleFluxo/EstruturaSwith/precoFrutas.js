let fruta = "banana";

switch(fruta){
  case "maça":
    console.log("Preço por quilo da maça é R$2,00");
    break;
  case "banana":
    console.log("Preço por quilo da banana é R$1,50");
    break;
  case "laranja":
    console.log("Preço por quilo da laranja é R$3,00");
    break;
  default:
    console.log("Fruta selecionada não esta disponivel ou preço não especificado!");        
}