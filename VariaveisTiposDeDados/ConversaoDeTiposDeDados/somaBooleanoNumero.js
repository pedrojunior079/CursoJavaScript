// Experimente diferentes valores
var booleanoTrue = true;
var booleanoFalse = false;
var numero = 10;

// Somando booleano true com um número
var resultadoTrue = booleanoTrue + numero;
console.log("Resultado (true + numero):", resultadoTrue);

// Somando booleano false com um número
var resultadoFalse = booleanoFalse + numero;
console.log("Resultado (false + numero):", resultadoFalse);
