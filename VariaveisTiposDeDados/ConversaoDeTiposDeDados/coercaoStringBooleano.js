// String contendo "false"
var stringFalse = "false";

// Utilizando a função Boolean()
var booleano1 = Boolean(stringFalse);
console.log("Utilizando Boolean():", booleano1);

// Utilizando o operador de negação !
var booleano2 = !stringFalse;
console.log("Utilizando ! (negação):", booleano2);

